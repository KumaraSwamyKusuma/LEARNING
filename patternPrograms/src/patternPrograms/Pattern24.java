package patternPrograms;

public class Pattern24 {

	public static void main(String[] args) {
         
		for(int i=1;i<=5;i++)

		{

		//in each line print spaces

		for(int k=1;k<=i;k++)

		{

		System.out.print(" ");

		}

		//in the same line continue printing *'s

		for(int j=1;j<=i;j++)

		{

		System.out.print("*");

		}

		System.out.println();

		}


	}

}
