package patternPrograms;

public class Pattern23 {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++)

		{

		//in each line print 5 spaces

		for(int k=1;k<=i;k++)

		{

		System.out.print(" ");

		}

		//in the same line continue printing 5 *'s

		for(int j=1;j<=5;j++)

		{

		System.out.print("*");

		}

		System.out.println();

		}

		}
		
}

